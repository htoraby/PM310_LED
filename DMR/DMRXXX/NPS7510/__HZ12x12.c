void GetHZ12x12Bitmap(WORD wNeiMa,WORD* pHZBitmap)
{
    short nFirst = 0;										// 二分法查找
    short nEnd = c_wHZCount-1;
    short nMid = (nFirst + nEnd)/2;
    WORD j;

    while(nFirst<=nEnd)
    {
        nMid = (nFirst + nEnd)/2;
        if(wNeiMa==c_wHZBitmap[nMid][0]) break;
        else if(wNeiMa>c_wHZBitmap[nMid][0]) nFirst = nMid + 1;
        else nEnd = nMid - 1;
    }
    for(j=1;j<13;j++) *pHZBitmap++ = c_wHZBitmap[nMid][j];
}

